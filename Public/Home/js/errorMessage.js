function getErrorMsg(errorCode) {
	var language = 'zh_CN';// getElementValue('languageStr');
	if (errors[language] && errors[language][errorCode]) {
		return trim(errors[language][errorCode]);
	} else {
		return trim(errors[language]['9999'] + '(' + errorCode + ')');
	}
}

function trim(str) {
	return str.replace(/(^\s*)|(\s*$)/g, "");
}

